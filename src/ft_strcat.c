#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int lenght;

	lenght = ft_strlen(s1);
	while (*s2 != '\0')
	{
		s1[lenght] = *s2;
		s2++;
		lenght++;
	}
	s1[lenght] = '\0';
	return (s1);
}
