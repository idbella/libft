#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int lenght;

	lenght = ft_strlen(s1);
	while (*s2 != '\0' && n-- > 0)
	{
		s1[lenght++] = *s2++;
	}
	s1[lenght] = '\0';
	return (s1);
}
